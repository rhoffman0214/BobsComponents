namespace BobsComponent.Library.Enums;

/// <summary>
/// Represents visual styling variants for buttons
/// </summary>
public enum ButtonStyle
{
    /// <summary>
    /// Primary action button (default blue/accent color)
    /// </summary>
    Primary,

    /// <summary>
    /// Secondary action button (gray/neutral color)
    /// </summary>
    Secondary,

    /// <summary>
    /// Tertiary action button (info/accent color)
    /// </summary>
    Tertiary,

    /// <summary>
    /// Success action button (green color)
    /// </summary>
    Success,

    /// <summary>
    /// Warning action button (yellow/orange color)
    /// </summary>
    Warning,

    /// <summary>
    /// Error/destructive action button (red color)
    /// </summary>
    Error
}
