/// <summary>
/// Determines how much code to display in CodeSnippet component
/// </summary>
public enum HighlightMode
{
    /// <summary>
    /// Show only the key parameters (minimal code)
    /// </summary>
    KeyParametersOnly,

    /// <summary>
    /// Show the full component markup with all parameters
    /// </summary>
    FullMarkup
}
